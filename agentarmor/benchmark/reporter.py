def print_terminal_table(run: BenchmarkRun) -> None:
    from rich.console import Console
    from rich.table import Table

    console = Console()
    console.print(f"\n[bold]AgentArmor Benchmark — {run.suite_name}[/bold]")
    console.print("━" * 48)
    table = Table(show_header=True, header_style="bold")
    table.add_column("Model", style="cyan")
    table.add_column("Pass Rate", justify="right")
    table.add_column("Risk Score", justify="right")
    for score in run.model_scores:
        table.add_row(
            score.target.label,
            f"{score.pass_rate * 100:.0f}%",
            f"{score.risk_score:.2f}",
        )
    console.print(table)
    console.print("━" * 48)


def write_json_report(run: BenchmarkRun, output_path: Path) -> Path:
    output_path.parent.mkdir(parents=True, exist_ok=True)
    output_path.write_text(
        json.dumps(run.model_dump(mode="json"), indent=2, default=str),
        encoding="utf-8",
    )
    return output_path


def write_html_leaderboard(run: BenchmarkRun, output_path: Path, *, version: str = "0.6.0") -> Path:
    from jinja2 import Template

    html = Template(_LEADERBOARD_HTML).render(run=run, version=version)
    output_path.parent.mkdir(parents=True, exist_ok=True)
    output_path.write_text(html, encoding="utf-8")
    return output_path


def default_report_paths(output_dir: Path, run: BenchmarkRun) -> tuple[Path, Path]:
    day = date.today().isoformat()
    html = output_dir / f"benchmark-{day}.html"
    json_path = output_dir / f"benchmark-{run.id}.json"
    return html, json_path
